const wallEl = document.getElementById('wall');
const formEl = document.getElementById('kudos-form');
const vibeBtn = document.getElementById('vibe-btn');
const vibeText = document.getElementById('vibe-text');
const vibeSource = document.getElementById('vibe-source');

function timeAgo(ts) {
  const seconds = Math.floor((Date.now() - ts) / 1000);
  if (seconds < 60) return 'just now';
  const minutes = Math.floor(seconds / 60);
  if (minutes < 60) return `${minutes}m ago`;
  const hours = Math.floor(minutes / 60);
  if (hours < 24) return `${hours}h ago`;
  return `${Math.floor(hours / 24)}d ago`;
}

function renderKudos(items) {
  wallEl.innerHTML = items
    .map(
      (k) => `
        <div class="kudos-card">
          <div class="meta"><b>${escapeHtml(k.from)}</b> &rarr; <b>${escapeHtml(k.to)}</b> &middot; ${timeAgo(k.ts)}</div>
          <div class="msg">${escapeHtml(k.message)}</div>
        </div>
      `
    )
    .join('');
}

function escapeHtml(str) {
  const div = document.createElement('div');
  div.textContent = str;
  return div.innerHTML;
}

async function loadKudos() {
  const res = await fetch('/api/kudos');
  const items = await res.json();
  renderKudos(items);
}

formEl.addEventListener('submit', async (e) => {
  e.preventDefault();
  const data = Object.fromEntries(new FormData(formEl).entries());
  await fetch('/api/kudos', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(data),
  });
  formEl.reset();
  loadKudos();
});

vibeBtn.addEventListener('click', async () => {
  vibeBtn.disabled = true;
  vibeText.textContent = 'Thinking...';
  vibeSource.textContent = '';
  try {
    const res = await fetch('/api/summary');
    const data = await res.json();
    vibeText.textContent = data.summary;
    vibeSource.textContent = data.source === 'openai' ? 'generated by OpenAI' : 'generated locally (no API key set)';
  } catch (err) {
    vibeText.textContent = 'Could not generate a summary right now.';
  } finally {
    vibeBtn.disabled = false;
  }
});

loadKudos();
setInterval(loadKudos, 5000);
